#include <bits/stdc++.h>
using namespace std;
int main()
{
    int arr[] = {100,21,42,95,88};  // We don't need to declare the size of the array if we are initializing it's value at the same time!

    // To get the size of array:-
    int size = sizeof(arr) / sizeof(int);
    cout<<"The size of the array is: "<<size<<endl;
    return 0;
}