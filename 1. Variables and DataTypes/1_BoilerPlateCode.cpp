#include <iostream> // Preprocessor Directives
using namespace std;    // Namespace Declaration - std
/* namespace includes the standard C++ library like cout, cin, endl etc. 
They are defined in the <iostream> & declared using the using namespace std; statement. */

#define PI 3.14 // Macros Definition

int main(){     // Main Function - Starting Point of Program
    // Start coding from here

    cout << "Hello World!\n"; // Output Statement - Print Hello World!
    cout << "The value of Pi is: " << PI << endl; // Output Statement - Print Value of PI

    return 0;  // Semi colon - Statement Terminator & Return Statement - End of Program
}   // Pair of Curly Braces - Block of Code 

